@tool
def policy_interpret(topic: str) -> str:
    """解读高考志愿填报相关政策和规则。
    
    Args:
        topic: 要了解的政策主题，如"平行志愿""3+1+2""冲稳保""退档"等
    
    Returns:
        相关政策的详细解读
    """
    # 模糊匹配
    matched = []
    for key, policy in POLICY_KNOWLEDGE.items():
        if topic in key or key in topic:
            matched.append(policy)
        elif any(word in policy["title"] for word in topic.split()):
            matched.append(policy)
        elif any(word in policy["content"] for word in topic if len(word) > 1):
            matched.append(policy)

    if not matched:
        # 返回所有可用主题
        available = ", ".join(POLICY_KNOWLEDGE.keys())
        return (f"未找到关于「{topic}」的政策解读。\n"
                f"可用主题: {available}\n"
                f"你也可以直接问具体问题，我会尽力回答。")

    results = []
    for policy in matched:
        results.append(f"📜 {policy['title']}")
        results.append(policy["content"])
        results.append("")

    return "\n".join(results)
